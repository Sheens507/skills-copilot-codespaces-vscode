function skillsMember() {
    var member = document.getElementById("member");
    var skills = document.getElementById("skills");
    var contact = document.getElementById("contact");
    var memberBottom = document.getElementById("member-bottom");
    var skillsBottom = document.getElementById("skills-bottom");
    var contactBottom = document.getElementById("contact-bottom");

    member.style.display = "block";
    skills.style.display = "none";
    contact.style.display = "none";
    memberBottom.style.display = "block";
    skillsBottom.style.display = "none";
    contactBottom.style.display = "none";
}
